console.log(`1===1 \t\t \-\> ${1===1}`);       // true
console.log(`1===\"1\" \t \-\> ${1==="1"}`); // false
console.log(`3\<2 \t\t \-\> ${3<2}`);          // false
console.log(`2\>0 \t\t \-\> ${2>0}`);          // true
console.log(`7\>\= \t\t \-\> ${7>=7}`);        // true 
console.log(`6\<\=5 \t\t \-\> ${6<=5}`);       // false
console.log(`3 \!\=\= 3 \t \-\> ${3!==3}`);     // false
console.log(`\"hola\" !== \"hola\"\-\> ${"hola"!=="hola"}`); // false
console.log(`4 \!\=\= "4" \t \-\> ${4 !== "4"}`);    // true
console.log(`false \=\=\= true \t \-\> ${false === true}`);  // false
console.log(`true \t\t \-\> ${true}`);         // true
console.log(`false \t\t \-\> ${false}`);       // false